package com.boe.tvdevice.socketKeepLine;

public class DownLoadFailBean {
    private String flag;
    private String realId;
    private String failProgramId;
    private String memory;
    private String method;

    public String getFlag() {
        return flag;
    }

    public void setFlag(String flag) {
        this.flag = flag;
    }

    public String getRealId() {
        return realId;
    }

    public void setRealId(String realId) {
        this.realId = realId;
    }

    public String getFailProgramId() {
        return failProgramId;
    }

    public void setFailProgramId(String failProgramId) {
        this.failProgramId = failProgramId;
    }

    public String getMemory() {
        return memory;
    }

    public void setMemory(String memory) {
        this.memory = memory;
    }

    public String getMethod() {
        return method;
    }

    public void setMethod(String method) {
        this.method = method;
    }
}
